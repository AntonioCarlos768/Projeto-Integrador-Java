    private void alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarActionPerformed
        // TODO add your handling code here:

        int nrItem = TabelaEntrega.getSelectedRow();
        if (nrItem == -1) {
            JOptionPane.showInputDialog(this, "Nenhuma Entrega foi selecionada");

        } else {

            EntregaCliente tela = new EntregaCliente(null, true);
            Pedido e = tabelalista.get(nrItem);
            
            tela.setEntrega(e);
            tela.verifica();
            tela.setVisible(true);
            if (tela.isSalvar()) {

          

                try {
                    controledao.alterar(tela.getEntrega());
                } catch (Exception ex) {
                    Logger.getLogger(Entregas.class.getName()).log(Level.SEVERE, null, ex);
                }
                atualizaTabela(true);
            }

        }

    }//GEN-LAST:event_alterarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        // TODO add your handling code here:

        int linha = TabelaEntrega.getSelectedRow();

        if (linha == -1) {

            JOptionPane.showMessageDialog(this,
                    "Nenhuma Entrega foi selecionada");
        } else {

            controledao.excluir(tabelalista.get(linha));

            tabelalista.remove(linha);

            atualizaTabela(true);

        }

    }//GEN-LAST:event_excluirActionPerformed

