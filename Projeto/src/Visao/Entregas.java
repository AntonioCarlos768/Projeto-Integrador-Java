    public void atualizaTabela(boolean flag) {
        DefaultTableModel modelo = (DefaultTableModel) TabelaEntrega.getModel();

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        if (flag) {

            tabelalista.clear();
            tabelalista.addAll(controledao.listarTodos());

        }

        for (Pedido e : tabelalista) {

            modelo.addRow(new Object[]{e.getId(), formato.format(e.getDataentrega()), e.getLocal(), e.getCliente().getNome(),
                 });
        }

    }

