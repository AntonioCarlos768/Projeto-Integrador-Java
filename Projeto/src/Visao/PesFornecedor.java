    public void atualizaTabela(boolean flag) {
        DefaultTableModel modelo = (DefaultTableModel) TabelaFornecedor.getModel();

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        tabelalista.clear();
        tabelalista.addAll(controledao.listarTodos());

        for (Fornecedor f : tabelalista) {
            modelo.addRow(new Object[]{ f.getNomeEmpresa(), f.getEmail(), f.getTelefone(), f.getCidade()  });
        }
    }

    
