    public void atualizaTabela(boolean flag) {
        DefaultTableModel modelo = (DefaultTableModel) TabelaCliente.getModel();

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        tabelalista.clear();
        tabelalista.addAll(controledao.listarTodos());

        for (Cliente c : tabelalista) {
            modelo.addRow(new Object[]{c.getIdcliente(), c.getNome(), c.getTelefone(), c.getEndereco()});
        }
    }

