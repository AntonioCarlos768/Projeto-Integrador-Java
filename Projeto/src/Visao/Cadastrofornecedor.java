    public Fornecedor getFornecedor() {
        f.setNomeEmpresa(TXTnome.getText());
        f.setTelefone(TXTtelefone.getText());
        f.setEmail(TXTemail.getText());
        f.setCidade(TXTcidade.getText());
        f.setUf(TXTuf.getText());
        f.setEndereco(TXTendereco.getText());
        f.setDataEntrega(DataEntrega.getDate());
        f.setCep(TXTcep.getText());
        return f;
    }

    public void setFornecedor(Fornecedor f) {

        this.f.setCodigoFornecedor(f.getCodigoFornecedor());
        TXTnome.setText(f.getNomeEmpresa());
        TXTtelefone.setText(f.getTelefone());
        TXTemail.setText(f.getEmail());
        TXTcidade.setText(f.getCidade());
        TXTuf.setText(f.getUf());
        TXTendereco.setText(f.getEndereco());
        DataEntrega.setDate(f.getDataEntrega());
        TXTcep.setText(f.getCep());
    }

