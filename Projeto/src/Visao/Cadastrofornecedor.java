    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (TXTnome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Um Nome deve ser digitado");
            TXTnome.requestFocus();
        } else if (TXTtelefone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Um Telefone deve ser digitado");
            TXTtelefone.requestFocus();
        } else if (TXTcidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "A Cidade deve ser digitada");
            TXTcidade.requestFocus();
        } else if (TXTendereco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Um endere√ßo deve ser digitado");
        } else if (TXTuf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Um Estado deve ser digitado");
            TXTuf.requestFocus();
        } else if (TXTcep.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Um CEP deve ser digitado");
        } else {
            this.salvar = true;
            setVisible(false);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

