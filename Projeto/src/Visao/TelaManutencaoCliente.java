    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int linha = TabelaCliente.getSelectedRow();

        if (linha == -1) {

            JOptionPane.showMessageDialog(this,
                    "Nenhum fornecedor foi selecionada");
        } else {

            // excluir a sala do banco
            controledao.excluir(tabelalista.get(linha));

            // remove o elemento que foi selecionado
            tabelalista.remove(linha);

            // atualiza a tabela
            atualizaTabela(true);

        }

        //limparCampo();

        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int nrItem = TabelaCliente.getSelectedRow();
        if (nrItem == -1) {
            JOptionPane.showInputDialog(this, "Nenhum Cliente foi selecionada");

        } else {

            Cadastrocliente tela = new Cadastrocliente(null, true);
           Cliente c = tabelalista.get(nrItem);
            tela.setCliente(c);
            tela.setVisible(true);
            if (tela.isSalvar()) {
                controledao.alterar(tela.getCliente());
                atualizaTabela(true);
            }

        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

