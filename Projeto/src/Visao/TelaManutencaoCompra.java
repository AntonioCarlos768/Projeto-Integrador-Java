    private void RemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoverActionPerformed
        // TODO add your handling code here:

        int nrItem = TabelaCompras.getSelectedRow();
        if (nrItem == -1) {
            JOptionPane.showMessageDialog(this, "Nenhum produto foi selecionada");

        } else {

            int certeza = JOptionPane.showConfirmDialog(null, "Deseja remover da lista",
                    "Remover", JOptionPane.YES_OPTION, JOptionPane.CANCEL_OPTION);

            if (certeza == JOptionPane.YES_OPTION) {

                ItemCompra Item = lista.get(nrItem);

                lista.remove(nrItem);

                atualizaTabela2(true);

            }

        }
    }//GEN-LAST:event_RemoverActionPerformed

    private void PassarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PassarActionPerformed
        // TODO add your handling code here:

        int nrItem = TabelaItens.getSelectedRow();
        if (nrItem == -1) {
            JOptionPane.showMessageDialog(this, "Nenhum produto foi selecionada");

        } else {

            ItemCompra i = new ItemCompra();

            String qtde;
            qtde = JOptionPane.showInputDialog("Informe a quantidade:");

            Producao p = tabelalista.get(nrItem);

            int a = p.getIdproducao();

            System.out.println("a");

            for (int j = 0; j < lista.size(); j++) {
                System.out.println("b");
                if (lista.get(j).getProducao().equals(p)) {

                    int f = JOptionPane.showConfirmDialog(this, Passar);

                    if (f == 0) {
                        lista.get(j).setQuantidade(lista.get(j).getQuantidade() + Integer.parseInt(qtde));
                        lista.get(j).setPreco(lista.get(j).getPreco() + (tabelalista.get(nrItem).getPreco() * Integer.parseInt(qtde)));
                        atualizaTabela2(false);
                        existe = true;
                    } else {
                    }

                } else {
                    existe = false;
                }

            };

            System.out.println("c");

            if (!existe) {
                i.setProducao(p);

                i.setCompra(c);

                lista.add(i);
            }

            i.setQuantidade(Integer.parseInt(qtde));
            i.setNome(p.getNome());

            calc = p.getPreco() * Float.parseFloat(qtde) + armazena;
            armazena = calc;
            txtTotal.setText(String.valueOf(armazena));

            //float preco = p.getPreco() * Float.parseFloat(qtde);
            i.setPreco(armazena);

            for (ItemCompra it : lista) {

                if (it.getCompra().equals(it.getCompra())) {

                    c.setValor(armazena);

                }

                atualizaTabela2(true);
                atualizaTabela(true);

            }

        }

    }//GEN-LAST:event_PassarActionPerformed

