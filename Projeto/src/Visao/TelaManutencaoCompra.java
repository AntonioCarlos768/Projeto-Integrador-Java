    public void atualizaTabela(boolean flag) {
        DefaultTableModel modelo = (DefaultTableModel) TabelaItens.getModel();

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        if (flag) {
            tabelalista.clear();
            tabelalista.addAll(controledao.listarTodos());
        }
        for (Producao d : tabelalista) {

            modelo.addRow(new Object[]{d.getIdproducao(), d.getNome(), d.getPreco()});
        }
    }

