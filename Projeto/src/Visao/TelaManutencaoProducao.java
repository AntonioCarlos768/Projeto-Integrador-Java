    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Producao p = new Producao();

        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o Nome");
            txtNome.requestFocus();
        } else if (txtPreco.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o Preço");
            txtPreco.requestFocus();
        } else if (txtMinimo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite a Quantidade Minima");
            txtMinimo.requestFocus();
        } else {
            p.setNome(txtNome.getText());
            p.setPreco(Float.parseFloat(txtPreco.getText()));
            p.setMinimo(Integer.parseInt(txtMinimo.getText()));
            controledao.inserir(p);
            atualizaTabela(true);
            limparCampos();

        }


    }//GEN-LAST:event_jButton3ActionPerformed

