    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        // TODO add your handling code here:

        int linha = Tabela.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Nenhum produto foi selecionada");
        } else {
            //terminar!!!!
            int aviso = JOptionPane.showConfirmDialog(null, "Deseja excluir?");

            if (aviso == JOptionPane.YES_OPTION) {
                controledao.excluir(tabelalista.get(linha));
                tabelalista.remove(linha);
                atualizaTabela(true);
            }
        }
        limparCampo();
    }//GEN-LAST:event_ExcluirActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        // TODO add your handling code here:

        int nrItem = Tabela.getSelectedRow();
        if (nrItem == -1) {
            JOptionPane.showMessageDialog(this, "Nenhum Ingrediente foi selecionad");

        } else {

            Producao p = new Producao();
            p.setNome(txtNome.getText());
            p.setPreco(Float.parseFloat(txtPreco.getText()));
            p.setMinimo(Integer.parseInt(txtMinimo.getText()));

            controledao.alterar(p);
            atualizaTabela(true);

        }
        limparCampo();
    }//GEN-LAST:event_AlterarActionPerformed

